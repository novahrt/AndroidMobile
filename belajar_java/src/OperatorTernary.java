public class OperatorTernary {
    public static void main(String[] args) {
        int angka = 10;

        // Menggunakan operator ternary untuk menentukan pesan berdasarkan kondisi
        String pesan = (angka > 5) ? "Angka lebih besar dari 5" : "Angka tidak lebih besar dari 5";

        System.out.println(pesan);

       
    }
}

